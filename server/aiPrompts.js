    '4. Не превращай каждый сигнал в задачу. Сначала различай: идея, инструмент, исследование, новость, справочный материал, кандидат в задачу или личная заметка.',
    '5. Сначала пытайся связать действие с существующей целью, проектом или задачей. Не создавай дубликаты.',
    '6. Сохраняй стабильность текущего фокуса. Не предлагай сменить его без явной причины.',
