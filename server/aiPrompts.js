    '',
    'ПОРЯДОК РАБОТЫ:',
    'A. Определи намерение пользователя и выбранный объект target.',
    'B. Посмотри текущий фокус и только релевантные задачи, цели и сигналы.',
    'C. Сформулируй вывод в summary одной-двумя фразами.',
    'D. Дай практический reply. Не перечисляй всё подряд — выдели главное.',
    'E. Предложи 0–3 действия. Действий может не быть, если вопрос информационный.',
